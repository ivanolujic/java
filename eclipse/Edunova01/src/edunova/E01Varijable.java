package edunova;

public class E01Varijable {

	public static void main(String[] args) {
		
		// varijabla je prostor u memoriji
		// java je type strict programm language
		// int je tip podatka
		// i je naziv varijable
		
		// deklaracija varijable
		int i;
		
		// primitivni tipovi https://docs.oracle.com/javase/tutorial/java/nutsandbolts/datatypes.html
		
		// dodjeljivanje vrijednosti
		// opereter =
		i = 7;
		
		// korištenje varijable
		System.out.println(i);
		
		// deklaracija i dodjeljivanje u jednom koraku
		int t = 3;
		
		int x,y,z;
		
		int k=3,l=7;
		
		int q = l;
		
		System.out.println(q);
		

	}

}
